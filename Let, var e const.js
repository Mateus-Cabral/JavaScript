// Diferenças entre declaração de variáveis

// var declara a variável com escopo global
var a = 3
console.log(typeof(a))


// let declara a variável com escopo local (usado em funções)
let b = 4


// const declara uma constante (espaço de memória inalterável)
const pi = 3.14


// variáveis podem ser reatribuídas e re-declaradas
var a = 30
b = 40


console.log(a, b)
